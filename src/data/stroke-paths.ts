/**
 * Stroke path data for the 3-stage overlay tracing system.
 *
 * Each character has an array of strokes.
 * Each stroke is an array of {x, y} points (normalized 0-1 within the canvas).
 * The rendering layer scales these to the actual canvas size.
 *
 * Points along each stroke act as:
 * - Stage 1 (Guided Trace): smooth bezier path for user to follow
 * - Stage 2 (Dot-to-Dot): numbered waypoints the user connects
 * - Stage 3 (Free Write): faint background reference only
 */

export interface StrokePoint {
  x: number;
  y: number;
}

export interface CharacterStroke {
  points: StrokePoint[];
}

export interface StrokeData {
  character: string;
  strokes: CharacterStroke[];
}

